'''
Mad lib game example with string manipulation
'''
def mad_lib():
    ''' Very simple Mad lib game example'''
    color = input ("What's your favorite color? ")
    plural_noum = input ("Enter a noum! ")
    celebrity = input ("And what's your favorite celebrity? ")

    print (f"Roses are {color}")
    print (f"{plural_noum} are blue")
    print (f"I love {celebrity}")

mad_lib()
